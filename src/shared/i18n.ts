export type I18nKey = LocaleKey;
export type Translator = (key: I18nKey, params?: TranslationParams) => string;

export function resolveLocale(preference: LocalePreference | undefined, systemLocale: string | undefined): ResolvedLocale {
  if (isResolvedLocale(preference)) {
    return preference;
  }
  const normalized = (systemLocale ?? "").replace("_", "-").toLowerCase();
  if (normalized === "zh" || normalized.startsWith("zh-cn") || normalized.startsWith("zh-hans") || normalized.startsWith("zh-sg")) {
    return "zh-CN";
  }
  if (normalized.startsWith("zh-tw") || normalized.startsWith("zh-hk") || normalized.startsWith("zh-mo") || normalized.startsWith("zh-hant")) {
    return "zh-TW";
  }
  if (normalized === "en" || normalized.startsWith("en-")) {
    return "en-US";
  }
  if (normalized === "ja" || normalized.startsWith("ja-")) {
    return "ja-JP";
  }
  if (normalized === "ko" || normalized.startsWith("ko-")) {
    return "ko-KR";
  }
  return "en-US";
}

export function createTranslator(locale: ResolvedLocale): Translator {
  const dictionary = dictionaries[locale];
  return (key, params) => formatMessage(dictionary[key], params);
}

export function t(locale: ResolvedLocale, key: I18nKey, params?: TranslationParams): string {
  return createTranslator(locale)(key, params);
}

export function formatDate(locale: ResolvedLocale, value: number | Date, options?: Intl.DateTimeFormatOptions): string {
  return new Intl.DateTimeFormat(locale, options).format(value);
}

export function formatNumber(locale: ResolvedLocale, value: number, options?: Intl.NumberFormatOptions): string {
  return new Intl.NumberFormat(locale, options).format(value);
}

export function formatFileSize(locale: ResolvedLocale, size: number): string {
  if (size < 1024) {
    return `${formatNumber(locale, size, { maximumFractionDigits: 0 })} B`;
  }
  if (size < 1024 * 1024) {
    return `${formatNumber(locale, size / 1024, { maximumFractionDigits: 1 })} KB`;
  }
  return `${formatNumber(locale, size / 1024 / 1024, { maximumFractionDigits: 1 })} MB`;
}

export function getLocaleDictionaries(): Record<ResolvedLocale, Dictionary> {
  return dictionaries;
}

function isResolvedLocale(locale: unknown): locale is ResolvedLocale {
  return typeof locale === "string" && (supportedLocales as readonly string[]).includes(locale);
}

function createDictionary(base: Dictionary, overrides: DictionaryOverrides = {}): Dictionary {
  return {
    ...base,
    ...overrides
  };
}

